/* Create Tables */

CREATE TABLE BBS
(
	NO number NOT NULL,
	TITLE nvarchar2(100) NOT NULL,
	CONTENT nvarchar2(2000) NOT NULL,
	HITCOUNT number,
	POSTDATE date DEFAULT SYSDATE,
	ID varchar2(10) NOT NULL,
	PRIMARY KEY (NO)
);


CREATE TABLE USERS
(
	ID varchar2(10) NOT NULL,
	PWD varchar2(10) NOT NULL,
	NAME nvarchar2(10),
	PRIMARY KEY (ID)
);



/* Create Foreign Keys */

ALTER TABLE BBS
	ADD FOREIGN KEY (ID)
	REFERENCES USERS (ID)
;

CREATE SEQUENCE SEQ_BBS
NOCACHE
NOCYCLE;

